
public class MathExample {
    public static void main(String[] args) {
        System.out.println("Math.PI: " + Math.PI); // Constant for Pi
        System.out.println("Math.E: " + Math.E); // Constant for Euler's number
        System.out.println("Math.abs(-5): " + Math.abs(-5)); // Absolute value
        System.out.println("Math.max(5, 10): " + Math.max(5, 10)); // Maximum of two numbers
        System.out.println("Math.min(5, 10): " + Math.min(5, 10)); // Minimum of two numbers
        System.out.println("Math.sqrt(16): " + Math.sqrt(16)); // Square root
        System.out.println("Math.pow(2, 3): " + Math.pow(2, 3)); // Power function (2^3)
        System.out.println("Math.random(): " + Math.random()); // Random number between 0.0 and 1.0
        System.out.println("Math.round(3.6): " + Math.round(3.6)); // Round to nearest integer
        System.out.println("Math.ceil(3.2): " + Math.ceil(3.2)); // Round up to nearest integer
        System.out.println("Math.floor(3.8): " + Math.floor(3.8)); // Round down to nearest integer
        System.out.println("Math.toDegrees(Math.PI): " + Math.toDegrees(Math.PI)); // Convert radians to degrees
        System.out.println("Math.toRadians(180): " + Math.toRadians(180)); // Convert degrees to radians
        System.out.println("Math.sin(Math.PI / 2): " + Math.sin(Math.PI / 2)); // Sine of an angle in radians
        System.out.println("Math.cos(Math.PI): " + Math.cos(Math.PI)); // Cosine of an angle in radians
        System.out.println("Math.tan(Math.PI / 4): " + Math.tan(Math.PI / 4)); // Tangent of an angle in radians
        System.out.println("Math.log(10): " + Math.log(10)); // Natural logarithm (base e)
        System.out.println("Math.log(9) / Math.log(3): " + (Math.log(10) / Math.log(3))); // Logarithm base 3
        System.out.println("Math.log10(100): " + Math.log10(100)); // Base 10 logarithm
        System.out.println("Math.log1p(1): " + Math.log1p(1)); // Natural logarithm of (1 + x)
        System.out.println("Math.exp(1): " + Math.exp(1)); // Exponential function (e^x)
    }
}
